i=0 
while True:
    i = 1
    if i == 5:
        continue  # Skip the rest of the loop when i is 5
    print(i)
    if i >= 10:
        break  # Exit the loop when i is 10 or greater
# This code will print numbers from 1 to 10, skipping the number 5.
# The `continue` statement skips the current iteration when `i` is 5, and
# the `break` statement exits the loop when `i` reaches 10 or greater.
# The output will be:
# 1
# 2
# 3
# 4
# 6
# 7
# 8
# 9

